{
	int sum_factor = ((uint8_t) start - (uint8_t) end); // * (start - end) / (start - end);
	if(lr)
	{
		sum_factor /= (x1 - x0);

		for(; x0 < x1; x1--)
		{
//			drawLine(x1, y0, x1, y1, start);
			start += sum_factor;
		}

	}
	else
	{
		sum_factor /= (y1 - y0);
		
		for(; y0 < y1; y1--)
		{
//			drawLine(x0, y1, x1, y1, start);
			start += sum_factor;
		}
	}
}


