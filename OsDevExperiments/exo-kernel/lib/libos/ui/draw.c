void _uidraw_draw_pixel(uint32_t x, uint32_t y, uint32_t cl)
{
	vesa->driver->ioctl(vesa, VESA_IOCTL_DRAW_PIXEL, x, y, cl);

	return;

	if (x<0 || x > render_x  || y<0 || render_y) return;
	x = (x*(render_bpp >> 3));
	y = (y*render_bpsl);
	
	register char * cTemp;
	cTemp = &vesa_mem[x+y];//render_buffer[x+y];
	cTemp[0] = cl & 0xff;
	cTemp[1] = (cl>>8) & 0xff;
	cTemp[2] = (cl>>16) & 0xff;
}





