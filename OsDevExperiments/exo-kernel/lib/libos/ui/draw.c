void _uidraw_draw_text(uint8_t size, char *text, uint32_t x, uint32_t y, uint32_t col)
{
	char *str = text;
	
	while(*str != '\0')
	{
		_uidraw_draw_letter(size, *str, x, y, col);
		x += 8;
		str++;
	}
}



void _uidraw_draw_letter(uint8_t size, char letter, uint32_t x, uint32_t y, uint32_t col)
{
	char *let_data;
	
	//if(size == 16) 
	let_data = default_font_8x16[letter];
	//else if (size == 8) let_data = default_font_8x8[letter];
	
	
	int i;
	int j;
	
	for(i = 0; i < size; i++)
	{
		for(j = 0; j < 8; j++)
		{			
			if((let_data[i] & (1 << j)) > 0)
				_uidraw_draw_pixel(x+j, y+i-4, (uint32_t) col);
		}
	}
}


void _uidraw_draw_line(uint32_t x0, uint32_t y0, uint32_t x1, uint32_t y1, uint32_t col)
{
	uint32_t x = x0;
	uint32_t y = y0;
	
	while(x < x1 || y < y1)
	{
		_uidraw_draw_pixel(x, y, col);
		
		if(x < x1) x++;
		if(y < y1) y++;
	}
}


void _uidraw_draw_rect(uint32_t x0, uint32_t y0, uint32_t x1, uint32_t y1, uint32_t col)
{
	_uidraw_draw_line(x0, y0, x0, y1, col);
	_uidraw_draw_line(x0, y0, x1, y0, col);
	
	_uidraw_draw_line(x1, y0, x1, y1, col);
	_uidraw_draw_line(x0, y1, x1, y1, col);	
}

void _uidraw_draw_rect_fill(uint32_t x0, uint32_t y0, uint32_t x1, uint32_t y1, uint32_t fill)
{
	while(y0 <= y1)
	{
		_uidraw_draw_line(x0, y0, x1, y0, fill);	
		y0++;
	}
}


void _uidraw_draw_rect_fill_gradient(uint32_t x0, uint32_t y0, uint32_t x1, uint32_t y1, uint32_t c1, uint32_t c2)
{
	uint32_t gr = (c1 - c2) / (y1 - y0);
	
	for(; y0 < y1; y1--)
	{
		_uidraw_draw_line(x0, y1, x1, y1, c1);
		c1 += gr;
	}
}


void _uidraw_draw_rect_bordered(uint32_t x0, uint32_t y0, uint32_t x1, uint32_t y1, 
								uint8_t border_pix, uint32_t border, uint32_t fill)
{
	_uidraw_draw_rect_fill(x0, y0, x1, y1, fill);
	_uidraw_draw_rect(x0, y0, x1, y1, border);
}


void _uidraw_draw_rect_bordered_gradient(uint32_t x0, uint32_t y0, uint32_t x1, uint32_t y1, 
								uint8_t border_pix, uint32_t border, uint32_t col1, uint32_t col2)
{
	_uidraw_draw_rect_fill_gradient(x0, y0, x1, y1, col1, col2);
	_uidraw_draw_rect(x0, y0, x1, y1, border);
}





